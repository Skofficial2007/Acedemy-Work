//Enter two numbers from keyboard. Write a program to check if the two numbers are equal

#include <stdio.h>
void main () 
{
  
int n1, n2;
  
printf ("Enter num 1: ");
  
scanf ("%d", &n1);
  
printf ("\nEnter num 2: ");
  
scanf ("%d", &n2);
  
 
if (n1 == n2)
    
    printf ("Yes, they are equal");
  
else
    
    printf ("No, they aren't equal");

}


