//If the marks of Robert in three subjects are 78,45 and 62 respectively (each out of 100 ), write a program to calculate his total marks and percentage marks.

#include <stdio.h>
void main () 
{

int a = 78, b = 45, c = 62;
  
double p;
  
p = ((a + b + c) / 300.0) * 100.0;
  
printf ("Percentage is: %lf", p);

} 
 
 
 
 
