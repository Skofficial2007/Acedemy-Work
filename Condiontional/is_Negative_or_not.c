/*
Write a program to find whether a entered no. is Negative or not 
*/

#include <stdio.h>

void main()
{
    float n;
    printf("Enter a number: ");
    scanf("%f", &n);
    if(n>0)
    {
        printf("No, It's not negative");
    }
    else
        printf("Yes, It's a negative no.");
} 
 
 
 
 

